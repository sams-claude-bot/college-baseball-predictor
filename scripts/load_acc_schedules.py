def main():
    print("\n" + "=" * 60)
    print("  ACC Schedule Loader - 2026 Season")
    print("=" * 60)
    
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    
    # First, add all ACC teams to database
    print("\n Adding ACC teams to database...")
    for team_id, team_name in ACC_TEAMS.items():
        cursor.execute("""
            INSERT INTO teams (id, name, conference) VALUES (?, ?, 'ACC')
            ON CONFLICT(id) DO UPDATE SET conference = 'ACC', name = ?
        """, (team_id, team_name, team_name))
        print(f"   {team_name}")
    
    games_added = {}
    teams_added = set()
    
    # Process each team's schedule
    print(f"\n Loading schedules for {len(SCHEDULES)} teams...")
    for team_id, games in SCHEDULES.items():
        team_games = 0
        for game in games:
            date_str, opponent_name, is_home, time = game
            
            date = parse_date(date_str)
            if not date:
                print(f"  Warning: Could not parse date '{date_str}' for {team_id}")
                continue
            
            opponent_id = normalize_team_name(opponent_name)
            if not opponent_id:
                print(f"  Warning: Could not normalize opponent '{opponent_name}'")
                continue
            
            # Ensure opponent team exists
            cursor.execute("SELECT id FROM teams WHERE id = ?", (opponent_id,))
            if not cursor.fetchone():
                teams_added.add(opponent_id)
                cursor.execute("""
                    INSERT OR IGNORE INTO teams (id, name) VALUES (?, ?)
                """, (opponent_id, opponent_name))
            
            # Determine home/away
            if is_home:
                home_team_id = team_id
                away_team_id = opponent_id
            else:
                home_team_id = opponent_id
                away_team_id = team_id
            
            # Create game ID
            game_id = f"{date}_{away_team_id}_{home_team_id}"
            
            # Check if it's a conference game (both teams in ACC)
            is_conference = 1 if opponent_id in ACC_TEAMS else 0
            
            # Insert game
            try:
                cursor.execute("""
                    INSERT OR IGNORE INTO games 
                    (id, date, time, home_team_id, away_team_id, is_conference_game, status)
                    VALUES (?, ?, ?, ?, ?, ?, 'scheduled')
                """, (game_id, date, time, home_team_id, away_team_id, is_conference))
                
                if cursor.rowcount > 0:
                    team_games += 1
            except sqlite3.IntegrityError:
                pass
        
        games_added[team_id] = team_games
        print(f"  {ACC_TEAMS.get(team_id, team_id)}: {team_games} games added")
    
    conn.commit()
    
    # Summary
    total_new = sum(games_added.values())
    print(f"\n=== SUMMARY ===")
    print(f"Teams with new games: {len([k for k,v in games_added.items() if v > 0])}")
    print(f"Total new games added: {total_new}")
    print(f"New opponent teams added: {len(teams_added)}")
    
    # Count total games now
    cursor.execute("SELECT COUNT(*) FROM games")
    total_games = cursor.fetchone()[0]
    print(f"Total games in database: {total_games}")
    
    # Count ACC teams with games
    cursor.execute("""
        SELECT t.name, COUNT(DISTINCT g.id) as game_count
        FROM teams t
        LEFT JOIN games g ON t.id = g.home_team_id OR t.id = g.away_team_id
        WHERE t.conference = 'ACC'
        GROUP BY t.id
        ORDER BY game_count DESC
    """)
    print("\nGames per ACC team:")
    for row in cursor.fetchall():
        print(f"  {row[0]}: {row[1]} games")
    
    conn.close()
    print("\n ACC schedule loading complete!")


if __name__ == "__main__":
    main()
